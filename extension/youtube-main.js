// Main-world content script: runs inside https://www.youtube.com/watch pages and
// reads the Innertube player data the page already hydrated.
//
// MV3 manifest-declared content scripts cannot `import`, so the pure helper logic
// is duplicated here; the canonical version lives in extension/lib/player-response.js
// and is exercised by unit tests. Keep them in sync.
(function () {
  const LIST_REQUEST_EVENT = "shadowing-space-extension:list-request";
  const EXTRACT_REQUEST_EVENT = "shadowing-space-extension:extract-request";
  const LIST_RESULT_TYPE = "shadowing-space-extension/list-result";
  const EXTRACT_RESULT_TYPE = "shadowing-space-extension/extract-result";

  function isPlainObject(value) {
    return typeof value === "object" && value !== null && !Array.isArray(value);
  }

  function tryParseJson(value) {
    if (typeof value !== "string") return null;
    try {
      const parsed = JSON.parse(value);
      return isPlainObject(parsed) ? parsed : null;
    } catch {
      return null;
    }
  }

  function readPlayerResponse() {
    const initial = window.ytInitialPlayerResponse;
    if (isPlainObject(initial)) return initial;

    const args = window.ytplayer?.config?.args;
    if (isPlainObject(args)) {
      if (isPlainObject(args.raw_player_response)) return args.raw_player_response;
      const parsed = tryParseJson(args.player_response);
      if (parsed) return parsed;
    }

    try {
      const flexy = document.querySelector("ytd-watch-flexy");
      const flexyResponse =
        flexy?.playerData?.playerResponse ?? flexy?.__data?.playerData?.playerResponse;
      if (isPlainObject(flexyResponse)) return flexyResponse;
    } catch {
      // querySelector can throw for custom elements before definition; ignore.
    }

    return null;
  }

  function getVideoIdFromLocation() {
    try {
      return new URL(window.location.href).searchParams.get("v") || null;
    } catch {
      return null;
    }
  }

  function labelFromName(name, fallback) {
    if (!name) return fallback;
    if (typeof name.simpleText === "string" && name.simpleText.trim()) {
      return name.simpleText.trim();
    }
    if (Array.isArray(name.runs)) {
      const merged = name.runs
        .map((item) => item?.text ?? "")
        .join("")
        .trim();
      if (merged) return merged;
    }
    return fallback;
  }

  function normalizeText(raw) {
    return raw.replace(/\r?\n/g, " ").replace(/\s+/g, " ").trim();
  }

  // HTML entity decoding without innerHTML — parses as HTML document and reads
  // textContent, which never executes inline scripts. Second pass handles the
  // nested encoding YouTube emits.
  function decodeHtml(value) {
    if (typeof value !== "string" || !value) return "";
    const parser = new DOMParser();
    const first = parser.parseFromString(value, "text/html").documentElement.textContent ?? "";
    if (!first.includes("&")) return first;
    return parser.parseFromString(first, "text/html").documentElement.textContent ?? first;
  }

  function getThumbnailUrl(videoDetails) {
    const thumbnails = Array.isArray(videoDetails?.thumbnail?.thumbnails)
      ? videoDetails.thumbnail.thumbnails
      : [];
    return (
      [...thumbnails].reverse().find((entry) => typeof entry?.url === "string" && entry.url)?.url ??
      ""
    );
  }

  function buildSummary(playerResponse) {
    const videoDetails = isPlainObject(playerResponse?.videoDetails)
      ? playerResponse.videoDetails
      : {};
    const rawTracks = playerResponse?.captions?.playerCaptionsTracklistRenderer?.captionTracks;
    const tracks = Array.isArray(rawTracks)
      ? rawTracks.map((track, index) => ({
          trackIndex: index,
          languageCode: typeof track?.languageCode === "string" ? track.languageCode : "",
          label: labelFromName(track?.name, track?.languageCode ?? `Track ${index + 1}`),
          isAutoGenerated: track?.kind === "asr",
        }))
      : [];
    return {
      video: {
        videoId: typeof videoDetails.videoId === "string" ? videoDetails.videoId : "",
        title: typeof videoDetails.title === "string" ? videoDetails.title : "",
        thumbnailUrl: getThumbnailUrl(videoDetails),
      },
      tracks,
    };
  }

  function parseJson3Segments(payloadText) {
    const payload = JSON.parse(payloadText);
    if (!Array.isArray(payload.events)) return [];

    return payload.events
      .filter((event) => Array.isArray(event.segs) && event.tStartMs !== undefined)
      .map((event) => {
        const startMs = Number(event.tStartMs);
        const durationMs = Number(event.dDurationMs ?? 0);
        const text = normalizeText(
          event.segs.map((segment) => decodeHtml(segment.utf8 ?? segment.text ?? "")).join(""),
        );

        return {
          startMs,
          endMs: startMs + Math.max(durationMs || 1_600, 200),
          text,
        };
      })
      .filter(
        (segment) =>
          Number.isFinite(segment.startMs) && segment.endMs > segment.startMs && segment.text,
      );
  }

  function parseXmlSegments(payloadText) {
    const xml = new DOMParser().parseFromString(payloadText, "text/xml");
    const textNodes = Array.from(xml.querySelectorAll("text"));

    return textNodes
      .map((node) => {
        const start = Number.parseFloat(node.getAttribute("start") ?? "");
        const duration = Number.parseFloat(node.getAttribute("dur") ?? "");
        const text = normalizeText(decodeHtml(node.textContent ?? ""));

        return {
          startMs: Math.round(start * 1_000),
          endMs: Math.round((start + Math.max(duration || 1.6, 0.2)) * 1_000),
          text,
        };
      })
      .filter(
        (segment) =>
          Number.isFinite(segment.startMs) && segment.endMs > segment.startMs && segment.text,
      );
  }

  function buildCandidateUrls(baseUrl, track) {
    const url = new URL(baseUrl);
    const seen = new Set();
    const candidates = [];

    function push(variant, transform) {
      const next = new URL(url);
      transform(next);
      const str = next.toString();
      if (seen.has(str)) return;
      seen.add(str);
      candidates.push({ variant, url: str });
    }

    // 1. Default (no fmt) — YouTube returns classic XML.
    push("xml-default", (u) => {
      u.searchParams.delete("fmt");
    });
    // 2. json3 — what YouTube's own player uses.
    push("json3", (u) => u.searchParams.set("fmt", "json3"));
    // 3. srv3 — legacy XML variant, sometimes bypasses newer silent-reject paths.
    push("srv3", (u) => u.searchParams.set("fmt", "srv3"));
    // 4. srv1 — oldest XML variant.
    push("srv1", (u) => u.searchParams.set("fmt", "srv1"));
    // 5. vtt — WebVTT format.
    push("vtt", (u) => u.searchParams.set("fmt", "vtt"));

    // For ASR tracks, make sure kind=asr is present on the URL (some endpoints
    // silent-reject ASR tracks if kind is missing).
    if (track?.kind === "asr") {
      push("json3+kind=asr", (u) => {
        u.searchParams.set("fmt", "json3");
        if (!u.searchParams.has("kind")) u.searchParams.set("kind", "asr");
      });
    }

    // 6. Original URL as-is.
    push("original", () => {});

    return candidates;
  }

  async function fetchTrackSegments(baseUrl, track) {
    const candidates = buildCandidateUrls(baseUrl, track);
    const attempts = [];

    for (const { variant, url: candidateUrl } of candidates) {
      const attempt = { variant, status: null, contentType: "", length: 0, preview: "" };
      attempts.push(attempt);

      let response;
      try {
        response = await fetch(candidateUrl, { credentials: "include" });
      } catch (error) {
        attempt.error = error instanceof Error ? error.message : String(error);
        continue;
      }
      attempt.status = response.status;
      attempt.contentType = response.headers.get("content-type") || "";

      if (!response.ok) continue;

      const payloadText = await response.text();
      attempt.length = payloadText.length;
      attempt.preview = payloadText.slice(0, 100).replace(/\s+/g, " ");

      if (!payloadText.trim()) continue;

      try {
        const jsonSegments = parseJson3Segments(payloadText);
        if (jsonSegments.length > 0) {
          attempt.parsed = `json3:${jsonSegments.length}`;
          return { segments: jsonSegments, attempts };
        }
      } catch {
        // Fall through to XML parsing.
      }

      const xmlSegments = parseXmlSegments(payloadText);
      if (xmlSegments.length > 0) {
        attempt.parsed = `xml:${xmlSegments.length}`;
        return { segments: xmlSegments, attempts };
      }

      attempt.parsed = "unrecognized";
    }

    // Summarize what we saw into a short human-readable error.
    const summary = attempts
      .map((a) => {
        if (a.error) return `${a.variant}=ERR(${a.error})`;
        return `${a.variant}=HTTP${a.status} ${a.length}b${a.parsed ? "/" + a.parsed : ""}`;
      })
      .join(", ");

    const err = new Error(
      `YouTube returned no usable captions across ${attempts.length} attempts. ${summary}`,
    );
    err.attempts = attempts;
    throw err;
  }

  function resolvePlayerAndVideoId() {
    const expectedVideoId = getVideoIdFromLocation();
    const playerResponse = readPlayerResponse();

    if (!playerResponse) {
      throw new Error(
        "Could not read YouTube player data. Try refreshing the page — SPA navigation sometimes leaves captions unreadable until a reload.",
      );
    }

    const videoDetails = playerResponse?.videoDetails;
    const playerVideoId = videoDetails?.videoId;
    const videoId = playerVideoId || expectedVideoId;
    if (!videoId) {
      throw new Error("Unable to determine the YouTube video ID.");
    }

    if (expectedVideoId && playerVideoId && expectedVideoId !== playerVideoId) {
      throw new Error(
        `Player data is still showing the previous video (${playerVideoId}) instead of ${expectedVideoId}. Please refresh the YouTube tab and try again.`,
      );
    }

    return { playerResponse, videoId };
  }

  async function extractPayload(trackIndex) {
    const { playerResponse, videoId } = resolvePlayerAndVideoId();
    const videoDetails = playerResponse?.videoDetails;
    const captionTracks = playerResponse?.captions?.playerCaptionsTracklistRenderer?.captionTracks;

    if (!Array.isArray(captionTracks) || captionTracks.length === 0) {
      throw new Error("No caption tracks were found for this video.");
    }

    const effectiveIndex =
      Number.isInteger(trackIndex) && trackIndex >= 0 && trackIndex < captionTracks.length
        ? trackIndex
        : 0;
    const track = captionTracks[effectiveIndex];

    if (!track?.baseUrl || !track.languageCode) {
      throw new Error("The selected caption track is missing required metadata.");
    }

    const { segments, attempts } = await fetchTrackSegments(track.baseUrl, track);

    return {
      payload: {
        version: 1,
        source: "shadowing-space-extension",
        exportedAt: new Date().toISOString(),
        video: {
          videoId,
          title: videoDetails?.title || document.title.replace(/\s*-\s*YouTube$/, ""),
          thumbnailUrl: getThumbnailUrl(videoDetails),
        },
        track: {
          languageCode: track.languageCode,
          label: labelFromName(track.name, track.languageCode),
          isAutoGenerated: track.kind === "asr",
          originTrackId: track.vssId || track.languageCode,
        },
        segments,
      },
      debug: { attempts },
    };
  }

  function post(type, ok, extras) {
    window.postMessage({ type, ok, ...extras }, window.location.origin);
  }

  window.addEventListener(LIST_REQUEST_EVENT, () => {
    try {
      const { playerResponse } = resolvePlayerAndVideoId();
      const summary = buildSummary(playerResponse);
      post(LIST_RESULT_TYPE, true, { summary });
    } catch (error) {
      post(LIST_RESULT_TYPE, false, {
        error: error instanceof Error ? error.message : String(error),
      });
    }
  });

  window.addEventListener(EXTRACT_REQUEST_EVENT, (event) => {
    const trackIndex =
      typeof event?.detail?.trackIndex === "number" ? event.detail.trackIndex : undefined;

    void (async () => {
      try {
        const result = await extractPayload(trackIndex);
        post(EXTRACT_RESULT_TYPE, true, {
          payload: result.payload,
          debug: result.debug,
        });
      } catch (error) {
        const debug =
          error && typeof error === "object" && "attempts" in error
            ? { attempts: /** @type {any} */ (error).attempts }
            : undefined;
        post(EXTRACT_RESULT_TYPE, false, {
          error: error instanceof Error ? error.message : String(error),
          debug,
        });
      }
    })();
  });
})();
